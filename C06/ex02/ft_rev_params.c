{
	int	n;
	int	i;

	n = argc - 1;
	i = 0;
	while (n > 0)
	{
		while (argv[n][i])
		{
			write (1, &argv[n][i], 1);
			i++;
		}
		write (1, "\n", 1);
		n--;
		i = 0;
	}
	return (0);
}
